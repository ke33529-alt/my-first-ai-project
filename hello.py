#!/usr/bin/env python3
"""
Скрипт выводит приветствие пользователю.
Принимает имя пользователя как аргумент командной строки.
"""

import sys


def main():
    # Проверяем, передан ли аргумент командной строки
    if len(sys.argv) < 2:
        print("Ошибка: имя пользователя не передано.")
        print("Использование: python hello.py <имя>")
        sys.exit(1)
    
    # Получаем имя пользователя из аргументов
    name = sys.argv[1]
    
    # Выводим приветствие
    print(f"Hello, {name}!")


if __name__ == "__main__":
    main()
